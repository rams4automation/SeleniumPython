# Range of Indexes
list1 = ["apple", "banana", "cherry", "orange", "kiwi", "melon", "mango"]
print(list1[2:5])

list2 = ["apple", "banana", "cherry", "orange", "kiwi", "melon", "mango"]
print(list2[:4])

list3 = ["apple", "banana", "cherry", "orange", "kiwi", "melon", "mango"]
print(list3[2:])

list4 = ["apple", "banana", "cherry", "orange", "kiwi", "melon", "mango"]
print(list4[-4:-1])


